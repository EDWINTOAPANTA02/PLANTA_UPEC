-- -----------------------------------------------------------------------------
-- 3. PROCESADORES AUTOMATIZADOS (TRIGGERS DE BASE DE DATOS)
-- -----------------------------------------------------------------------------

PROMPT Creando trigger TRG_PFI_OPERACIONES_CALC...
CREATE OR REPLACE TRIGGER TRG_PFI_OPERACIONES_CALC
BEFORE INSERT OR UPDATE ON PFI_TAB_OPERACIONES
FOR EACH ROW
DECLARE
    v_precio PFI_TAB_INVENTARIO.PRECIO_VENTA_SUGERIDO%TYPE;
    v_cat    PFI_TAB_INVENTARIO.CATEGORIA%TYPE;
BEGIN
    -- Solo calcular dinero si es una Venta Pública
    IF :NEW.TIPO_OPERACION = 'Venta' THEN
        SELECT PRECIO_VENTA_SUGERIDO, CATEGORIA 
          INTO v_precio, v_cat
          FROM PFI_TAB_INVENTARIO
         WHERE ID = :NEW.ID_ITEM_INVENTARIO;
        
        -- Validar regla de negocio de Materiales
        IF v_cat = 'Material' THEN
            raise_application_error(-20001, 'REGLA DE NEGOCIO: No se admiten transacciones monetarias o ventas para items de categoria Material.');
        END IF;
        
        :NEW.MONTO_TOTAL_DINERO := :NEW.CANTIDAD * NVL(v_precio, 0);
    ELSE
        :NEW.MONTO_TOTAL_DINERO := 0;
    END IF;
END;
/


