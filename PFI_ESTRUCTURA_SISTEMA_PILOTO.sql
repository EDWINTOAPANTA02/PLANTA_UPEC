-- -----------------------------------------------------------------------------
-- 3. DISPARADORES AUTOMÁTICOS (REFACTORIZADOS)
-- -----------------------------------------------------------------------------

PROMPT Creando trigger TRG_PFI_OPERACIONES_CALC...
CREATE OR REPLACE TRIGGER TRG_PFI_OPERACIONES_CALC
BEFORE INSERT OR UPDATE ON PFI_TAB_OPERACIONES
FOR EACH ROW
DECLARE
    v_precio PFI_TAB_INVENTARIO.PRECIO_VENTA_SUGERIDO%TYPE;
    v_cat    PFI_TAB_INVENTARIO.CATEGORIA%TYPE;
BEGIN
    IF :NEW.TIPO_OPERACION = 'Academico' THEN
        :NEW.MONTO_TOTAL_DINERO := 0;
    ELSIF :NEW.TIPO_OPERACION = 'Venta' THEN
        SELECT NVL(PRECIO_VENTA_SUGERIDO, 0), CATEGORIA 
          INTO v_precio, v_cat
          FROM PFI_TAB_INVENTARIO
         WHERE CODIGO = :NEW.CODIGO_ITEM_INVENTARIO;
        
        IF v_cat = 'Material' THEN
            raise_application_error(-20001, 'REGLA DE NEGOCIO: No se admiten ventas monetarias para la categoria Material.');
        END IF;
        
        :NEW.MONTO_TOTAL_DINERO := :NEW.CANTIDAD * v_precio;
    END IF;
EXCEPTION
    WHEN NO_DATA_FOUND THEN
        raise_application_error(-20002, 'ERROR: El ítem no existe en la Bodega Central.');
END;
/

PROMPT Creando trigger TRG_PFI_OPERACIONES_STOCK...
CREATE OR REPLACE TRIGGER TRG_PFI_OPERACIONES_STOCK
AFTER INSERT ON PFI_TAB_OPERACIONES
FOR EACH ROW
BEGIN
    UPDATE PFI_TAB_INVENTARIO
       SET STOCK = STOCK - :NEW.CANTIDAD
     WHERE CODIGO = :NEW.CODIGO_ITEM_INVENTARIO;
END;
/


