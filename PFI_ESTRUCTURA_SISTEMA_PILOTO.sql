    CODIGO                 NUMBER GENERATED BY DEFAULT AS IDENTITY,
    CODIGO_PLANTA          NUMBER NOT NULL,
    CATEGORIA              VARCHAR2(20) NOT NULL,
    NOMBRE                 VARCHAR2(250) NOT NULL,
