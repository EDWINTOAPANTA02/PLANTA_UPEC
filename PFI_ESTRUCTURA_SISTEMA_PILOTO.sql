    -- Restricciones
    CONSTRAINT PK_PFI_EQUIPOS PRIMARY KEY (ID),
    CONSTRAINT FK_PFI_EQUIPOS_PLANTAS FOREIGN KEY (ID_PLANTA) REFERENCES PFI_TAB_PLANTAS(ID),
    CONSTRAINT UQ_PFI_EQUIPOS_COD UNIQUE (CODIGO_INSTITUCIONAL),
    CONSTRAINT CHK_PFI_EQUIPOS_ESTADO CHECK (ESTADO_TECNICO IN ('OPERATIVO', 'MANTENIMIENTO', 'INACTIVO')),
    CONSTRAINT CHK_PFI_EQUIPOS_PROY CHECK (PERTENECE_PROYECTO IN ('SI', 'NO')),
