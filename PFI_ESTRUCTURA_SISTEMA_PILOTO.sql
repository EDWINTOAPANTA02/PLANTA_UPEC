    CONSTRAINT CHK_PFI_INVENTARIO_UNIDAD CHECK (UNIDAD_MEDIDA IN ('Kg', 'g', 'ml', 'L', 'unid', 'Unidades')),
    CONSTRAINT CHK_PFI_INVENTARIO_EST_MAT CHECK (ESTADO_MATERIAL IN ('Buen estado', 'Mal estado')),
