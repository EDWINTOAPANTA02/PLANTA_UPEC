PROMPT Creando tabla PFI_TAB_INVENTARIO...
CREATE TABLE PFI_TAB_INVENTARIO (
    ID                     NUMBER GENERATED BY DEFAULT AS IDENTITY,
    ID_PLANTA              NUMBER NOT NULL,
    CATEGORIA              VARCHAR2(20) NOT NULL,
    NOMBRE                 VARCHAR2(250) NOT NULL,
    STOCK                  NUMBER(12, 2) DEFAULT 0 NOT NULL,
    UNIDAD_MEDIDA          VARCHAR2(20) NOT NULL,
    FECHA_VENCIMIENTO      DATE,
    ESTADO_MATERIAL        VARCHAR2(20),
    COSTO_UNITARIO         NUMBER(12, 2),
    PRECIO_VENTA_SUGERIDO  NUMBER(12, 2),
    OBSERVACIONES          VARCHAR2(4000),
    -- Restricciones
    CONSTRAINT PK_PFI_INVENTARIO PRIMARY KEY (ID),
    CONSTRAINT FK_PFI_INVENTARIO_PLANTAS FOREIGN KEY (ID_PLANTA) REFERENCES PFI_TAB_PLANTAS(ID),
    CONSTRAINT CHK_PFI_INVENTARIO_CAT CHECK (CATEGORIA IN ('Insumo', 'Material')),
    CONSTRAINT CHK_PFI_INVENTARIO_UNIDAD CHECK (UNIDAD_MEDIDA IN ('Kg', 'g', 'ml', 'L', 'unid', 'Unidades')),
    CONSTRAINT CHK_PFI_INVENTARIO_EST_MAT CHECK (ESTADO_MATERIAL IN ('Buen estado', 'Mal estado')),
    CONSTRAINT CHK_PFI_INVENTARIO_STOCK CHECK (STOCK >= 0),
    -- Regla de Negocio: Materiales no manejan costo ni precio
    CONSTRAINT CHK_PFI_INVENTARIO_MONEY_MAT CHECK (
        (CATEGORIA = 'Material' AND COSTO_UNITARIO IS NULL AND PRECIO_VENTA_SUGERIDO IS NULL) OR 
        (CATEGORIA = 'Insumo')
    )
);

CREATE INDEX IX_PFI_INVENTARIO_PLANTA ON PFI_TAB_INVENTARIO(ID_PLANTA);

